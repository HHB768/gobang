class printer {
public:
    using M_type = std::vector<std::list<int>>;
    
    template <typename... Args>
    static M_type concat(const M_type& m1, const M_type& m2,
                         Args&&... args) {
        return concat(concat(m1, m2), std::forward<Args>(args)...);
    }
    static M_type concat(const M_type& m1, const M_type& m2) {
        return concat(_copy(m1), m2);
    }
    static M_type concat(M_type&& m1, const M_type& m2) {
        assert(m1.size() == m2.size());
        for (int i = 0; i < m1.size(); i++) {
            m1[i].insert(m1[i].end(), m2[i].begin(), m2[i].end());
        }
        return m1;
    }  // CHECK: is it works?

    static void roll(M_type& m, const int n) {
        for (auto&& line : m) {
            for (int i = 0; i < n; i++) {
                auto el = line.front();
                line.pop_front();
                line.emplace_back(el);
            }
        }
    }
    static M_type get_xq4_matrix() {
        return concat(matrix_X, matrix_Q, matrix_4, matrix__, matrix_G, matrix_B, matrix_empty);
    }

private:
    static M_type _copy(const M_type& m1) {
        return M_type(m1);
    }
};  // endof class printer



